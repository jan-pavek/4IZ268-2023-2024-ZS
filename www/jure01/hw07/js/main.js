//              0123456789...
const alphabet = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ';

document.getElementById("messageInput").value = "Enter the encrypted text here";
document.getElementById("keyInput").value = "Enter the key value here";


function shiftChar(c, shift) {
    const index = alphabet.indexOf(c.toUpperCase());
    if (index === -1) return c; // if character not in alphabet, return it unchanged
    let shiftedIndex = (index - shift) % alphabet.length;
    // handling negative indices (loop back to end of the alphabet)
    if (shiftedIndex < 0) shiftedIndex += alphabet.length;
    return alphabet[shiftedIndex];
}

function shiftString(str, shift) {
    return str
        .split('')
        .map(char => shiftChar(char, shift))
        .join('');
}

function caesarDecipher(cipherText, usedKey) {
    return shiftString(cipherText, usedKey);
}

function decipher() {
    const message = document.getElementById('messageInput').value;
    const key = parseInt(document.getElementById('keyInput').value);

    const decipheredText = caesarDecipher(message, key);
    console.log(decipheredText); // Display in console

    // Display on the page
    const outputDiv = document.getElementById('output');
    outputDiv.innerHTML = `${decipheredText}`;
}

